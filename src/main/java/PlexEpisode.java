class PlexEpisode {
    private String title;
    private int parentIndex;
    private int index;

    String getTitle() {
        return title;
    }

    void setTitle(String input) {
        title = input;
    }

    int getParentIndex() {
        return parentIndex;
    }

    void setParentIndex(int input) {
        parentIndex = input;
    }

    int getIndex() {
        return index;
    }

    void setIndex(int input) {
        index = input;
    }

}
